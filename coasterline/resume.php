				<h1><a href="">Tips To Prepare Good Resume !!!!</a></h1>
								<div class="col-md-7" style="margin-top:20px;">
<ul>
<li class="ib-green">1. Know the purpose of your resume</li>
<li>Some people write a resume as if the purpose of the document was to land a job. As a result they end up with a really 
long and boring piece that makes them look like desperate job hunters. 
The objective of your resume is to land an interview, and the interview will land you the job (hopefully!).
</li><section class="bottom-border"></section>
<li class="ib-green">2. Back up your qualities and strengths :</li>
<li>Instead of creating a long (and boring) list with all your qualities 
(e.g., disciplined, creative, problem solver) try to connect them with real life and work experiences. 
In other words, you need to back these qualities and strengths up, else it will appear that you are just trying to inflate things.
</li>
</li><section class="bottom-border"></section>
<li class="ib-green">3. Make sure to use the right keywords :</li>
<li>Most companies (even smaller ones) are already using digital databases to search for candidates. 
This means that the HR department will run search queries based on specific keywords. Guess what, if your resume doesnot have the 
keywords related to the job you are applying for, you will be out even before the game starts.
</li>
<section class="bottom-border"></section>
<li class="ib-green">Leave lift go by stairs:</li>
<li>So you take the stairs? You have heard that one must take the stairs. But did someone tell 
you that just taking the stairs is not enough. Every step can give good results if done in a desired manner. 
The best way to climb the stairs is to run on it or take two at a time. 
Experts say that it also slows down the ageing process.
These keywords will usually be nouns. Check the job description and related job ads for a clue on what 
the employer might be looking for. You can read more about resume 
keywords on the article Tapping the Power of Keywords to Enhance Your Resumes Effectiveness.
</li><section class="bottom-border"></section>
<li class="ib-green">4. Use effective titles:</li>
<li>Like it or not, employers will usually make a judgment about your resume in 5 seconds. 
Under this time frame the most important aspect will be the titles that you listed on the resume, so make sure they grab the attention. Try to be as descriptive as possible, giving the employer 
a good idea about the nature of your past work experiences. <div class="clear"></div>
<b>For example:</b><div class="clear"></div>
<b>Bad title:</b> Accounting<div class="clear"></div>
<b>Good title:</b> Management of A/R and A/P and Recordkeeping
</li><section class="bottom-border"></section>
<li class="ib-green">5. Proofread it twice :</li>
<li>It would be difficult to emphasize the importance of proofreading your resume. 
One small typo and your chances of getting hired could slip. Proofreading it once is not enough, so 
do it twice, three times or as many as necessary. If you do not know how to proofread effectively, here are 8 tips that you can use.
</li><section class="bottom-border"></section>

<li class="ib-green">5. Proofread it twice :</li>
<li>It would be difficult to emphasize the importance of proofreading your resume. 
One small typo and your chances of getting hired could slip. Proofreading it once is not enough, so 
do it twice, three times or as many as necessary. If you do not know how to proofread effectively, here are 8 tips that you can use.
</li><section class="bottom-border"></section>

<li class="ib-green">6. Use bullet points :</li>
<li>No employer will have the time (or patience) to read long paragraphs of text. 
Make sure, therefore, to use bullet points and short sentences to 
describe your experiences, educational background and professional objectives.
</li><section class="bottom-border"></section>

<li class="ib-green">7. Where are you going ? :</li>
<li>Including professional goals can help you by giving employers an idea of where you are going, and 
how you want to arrive there. You do not need to have a special section devoted to your professional objectives, 
but overall the resume must communicate it. The question of whether or not to highlight your career objectives on the resume is a polemic one among HR managers, 
so go with your feeling. If you decide to list them, make sure they are not generic.
</li>
<section class="bottom-border"></section>

<li class="ib-green">8. Put the most important information first :</li>
<li>This point is valid both to the overall order of your resume, as well as to the individual sections. 
Most of the times your previous work experience will be the most important part of the resume, so put it at the top. 
When describing your experiences or
 skills, list the most important ones first.
</li>
<section class="bottom-border"></section>

<li class="ib-green">9. Attention to the typography :</li>
<li>First of all make sure that your fonts are big enough. The smaller you should go is 11 points, but 12 is 
probably safer. Do not use capital letters all over the place, 
remember that your goal is to communicate a message as fast and as clearly as possible. Arial and Times are good choices.
</li>
<section class="bottom-border"></section>

<li class="ib-green">10. Do not include "no kidding" information :</li>
<li>There are many people that like to include statements like
 "Available for interview" or "References available upon request." If you are sending a resume to a company, 
 it should be a given that you are available for an interview and that you will
 provide references if requested. Just avoid items that will make the employer think "no kidding!"
</li>
<section class="bottom-border"></section>

<li class="ib-green">11. Explain the benefits of your skills :</li>
<li>Merely stating that you can do something will not catch the attention of the employer. If you manage to explain how it will benefit 
his company, and to connect it to tangible results, then you will greatly improve your chances.
</li>
<section class="bottom-border"></section>

<li class="ib-green">12. Avoid negativity :</li>
<li>
Do not include information that might sound negative in the eyes of the employer. This is valid both to your resume and to interviews. 
You do not need to include, for instance, things that you hated about your last company.
</li>
<section class="bottom-border"></section>

<li class="ib-green">13. Achievements instead of responsibilities :</li>
<li>Resumes that include a long list of "responsibilities included" are plain boring, and not efficient in selling yourself. 
Instead of listing responsibilities, therefore, describe your professional achievements.
</li>
<section class="bottom-border"></section>

<li class="ib-green">14. No pictures</li>
<li>Sure, we know that you are good looking, but unless you are applying for a job where the 
physical traits are very important (e.g., modeling, acting and so on), and unless 
the employer specifically requested it, you should avoid attaching your picture to the resume.
</li>
<section class="bottom-border"></section>

<li class="ib-green">15. Use numbers :</li>
<li>This tip is a complement to the 13th one. If you are going to describe your past professional achievements, 
it would be a good idea to make them as solid as possible. Numbers are your friends here. Do not merely mention that you increased 
the annual revenues of your division, say that you increased them by $100,000, by 78%, and so on.
</li>
<section class="bottom-border"></section>

<li class="ib-green">16. One resume for each employer :</li>
<li>One of the most common mistakes that people make is to create a standard resume and send it to all the job 
openings that they can find. Sure it will save you time, but it will also greatly decrease the chances of landing an 
interview (so in reality it could even represent a waste of time). 
Tailor your resume for each employer. The same point applies to your cover letters.
</li>
<section class="bottom-border"></section>

<li class="ib-green">17. Identify the problems of the employer :</li>
<li>A good starting point to tailor your resume for a specific employer is to identify what possible problems he might have 
at hand. Try to understand the market of the company you are applying for a job, and identify what kind of difficulties they might be going through. 
After that illustrate on your resume how you and your skills would help to solve those problems.</li>
<section class="bottom-border"></section>

<li class="ib-green">18. Avoid age discrimination :</li>
<li>It is illegal to discriminate people because of their age, but some employers do these considerations nonetheless. 
Why risk the trouble? Unless specifically requested, do not include your age on your resume.</li>
<section class="bottom-border"></section>

<li class="ib-green">19. You do not need to list all your work experiences :</li>
<li>If you have job experiences that you are not proud of, or that are not relevant to the current opportunity, you should just omit them.
 Mentioning that you used to sell hamburgers when you were 17 is probably not going to help you land that executive position.</li>
<section class="bottom-border"></section>

<li class="ib-green">20. Go with what you got :</li>
<li>If you never had any real working experience, just include your summer jobs or volunteer work. If you do not have a degree yet, 
mention the title and the estimated date for completion. As long as those points 
are relevant to the job in question, it does not matter if they are official or not.</li>
<section class="bottom-border"></section>

<li class="ib-green">21. Sell your fish :</li>
<li>
Remember that you are trying to sell yourself. As long as you do not go over the edge, all the marketing efforts that you can put in your resume 
(in its content, design, delivery method and so on) will give you an advantage over the other candidates.</li>
<section class="bottom-border"></section>

<li class="ib-green">22. Do not include irrelevant information</li>
<li>Irrelevant information such as political affiliation, religion and sexual preference will not help you. 
In fact it might even hurt your chances of landing an interview. Just skip it.</li>
<section class="bottom-border"></section>

<li class="ib-green">23. Use Mr. and Ms. if appropriate :</li>
<li>If you have a gender neutral name like Alex or Ryan make sure to include the Mr. or Ms. prefix, 
so that employers will not get confused about your gender.</li>
<section class="bottom-border"></section>

<li class="ib-green">24. No lies, please :</li>
<li>Seems like a no brainer, but you would be amused to discover the amount of people that lie in their resumes. 
Even small lies should be avoided. Apart from being wrong,
 most HR departments do background checks these days, and if you are buster it might ruin your credibility for good.</li>
<section class="bottom-border"></section>

<li class="ib-green">25. Keep the salary in mind :</li>
<li>The image you will create with your resume must match the salary and responsibility level that you are aiming for.</li>
<section class="bottom-border"></section>

<li class="ib-green">26. Analyze job ads :</li>
<li>You will find plenty of useful information on job ads. Analyze no only the ad that you will be applying for, but also those from companies on the same segment or offering related positions.
 You should be able to identify what profile they are looking for and how the information should be presented.</li>
<section class="bottom-border"></section>


<li class="ib-green">27. Get someone else to review your resume :</li>
<li>Even if you think you resume is looking kinky, it would be a good idea to get a second and third opinion about it. 
We usually become blind to our own mistakes or way of reasoning,
 so another people will be in a good position to evaluate the overall quality of your resume and make appropriate suggestions.</li>
<section class="bottom-border"></section>

<li class="ib-green">28. One or two pages :</li>
<li>The ideal length for a resume is a polemic subject. Most employers and recruiting specialists, however, 
say that it should contain one or two pages at maximum.
 Just keep in mind that, provided all the necessary information is there, the shorter your resume, the better.</li>
<section class="bottom-border"></section>

<li class="ib-green">29. Use action verbs :</li>
<li>A very common advice to job seekers is to use action verbs. But what are they? Action verbs are basically verbs
 that will get noticed more easily, and that will clearly communicate what your experience or achievement were. Examples include managed,
 coached, enforced and planned. Here you can find a complete list of action verbs divided by skill category.</li>
<section class="bottom-border"></section>

<li class="ib-green">30. Use a good printer :</li>
<li>If you are going to use a paper version of your resume, make sure to use a decent printer. 
Laser printers usually get the job done. Plain white paper is the preferred one as well.</li>
<section class="bottom-border"></section>

<li class="ib-green">31. No hobbies :</li>
<li>Unless you are 100% sure that some of your hobbies will support you candidacy, avoid mentioning them.
 I know you are proud of your swimming team, but share it with your friends and not with potential employers.</li>
<section class="bottom-border"></section>

<li class="ib-green">32. Update your resume regularly :</li>
<li>It is a good idea to update your resume on a regular basis. Add all the new information that you think is relevant,
 as well as courses, training programs and other academic qualifications that you might receive along the way.
 This is the best way to keep track of everything and to make sure that you will not end up sending an obsolete document to the employer.</li>
<section class="bottom-border"></section>

<li class="ib-green">33. Mention who you worked with :</li>
<li>
If you have reported or worked with someone that is well known in your industry, it could be a good idea to mention it on the resume.
 The same thing applies to presidents and CEOs. If you reported to or worked directly with highly ranked executives, add it to the resume.</li>
<section class="bottom-border"></section>

<li class="ib-green">34. No scattered information :</li>
<li>Your resume must have a clear focus. If would cause a negative impression if you mentioned that one 
year you were studying drama, and the next you were working as an accountant.
 Make sure that all the information you will include will work towards a unified image. Employers like decided people.</li>
<section class="bottom-border"></section>

<li class="ib-green">35. Make the design flow with white space :</li>
<li>Do not jam your resume with text. Sure we said that you should make your resume as short and concise as possible,
 but that refers to the overall amount of information and not to how much text you
 can pack in a single sheet of paper. White space between the words, lines and paragraphs can improve the legibility of your resume.</li>
<section class="bottom-border"></section>

<li class="ib-green">36. Careful with sample resume templates :</li>
<li>There are many websites that offer free resume templates. While they can help you to get an idea of what you are looking for,
 do not just copy and 
paste one of the most used ones. You certainly do not want to look just like any other candidate, do you?</li>
<section class="bottom-border"></section>

<li class="ib-green">37. Create an email proof formatting :</li>
<li>
It is very likely that you will end up sending your resume via email to most companies. Apart from having
 a Word document ready to go as an attachment, you should also have a text version of your resume that does not look disfigured
  in the body of the email or in online forms.
 Attachments might get blocked by spam filters, and many people just prefer having the resume on the body of the email itself.</li>
<section class="bottom-border"></section>

<li class="ib-green">38. Consider getting professional help :</li>
<li>If you are having a hard time to create your resume, or if you are receiving no response whatsoever from companies,
 you could consider hiring a professional resume writing service.
 There are both local and online options are available, and usually the investment will be worth the money.</li>
<section class="bottom-border"></section>

</ul>
