<p><b class="color-blue">What goes online, stays online</b> - Use privacy settings to make sure only your friends and family can see photos you post.
 Avoid posting holiday plans as criminals have been known to track your movements</p>
<p><b class="color-blue">Check your security and privacy settings</b> - Make sure your social network privacy settings are secured so 
