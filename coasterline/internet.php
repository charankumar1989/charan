 may guess such as your pets name. Use a mixture of letters, numbers and upper and lower case characters</p>
 
<p><b class="color-blue">Always protect your mobile device</b> - Make sure your mobile phone is pin-protected so all your personal 
information stored on it is safe. Download a security app which allows you to remotely wipe any personal data, should your mobile be lost or stolen</p>
 
<p><b class="color-blue">Do not talk to strangers online or offline</b> - Do not meet up with strangers and let your parents know if a 
