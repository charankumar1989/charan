<h5 class="color-blue">Drugs</h5>
<p>1. Temporary or permanent hair loss can be caused by several medications, including those for blood pressure problems, diabetes, 
heart disease and cholesterol. Any that affect the bodys hormone balance can have a pronounced effect: 
these include the contraceptive pill, hormone replacement therapy, steroids and acne medications.</p>
<p>2. Some treatments used to cure mycotic infections can cause massive hair loss.</p>
