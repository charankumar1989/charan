<h5 class="color-blue">Psychological</h5>
<p>Hair thinning and baldness cause psychological stress due to its effect on appearance. Although societal interest 
in appearance has a long history, this particular branch of psychology came into its own during the 1960s and has gained momentum as 
messages associating physical attractiveness with success and happiness grow more prevalent.</p>
<p>The psychology of hair thinning is a complex issue. Hair is considered an essential part of overall identity: 
especially for women, for whom it often represents femininity and attractiveness. Men typically associate a 
full head of hair with youth and vigor. Although they may be aware of pattern baldness in their family, many are 
uncomfortable talking about the issue. Hair thinning is therefore a sensitive issue for both sexes. For sufferers, it can represent a 
loss of control and feelings of isolation. People experiencing hair thinning often find themselves in a situation where their physical 
appearance is at odds with their own self-image and commonly worry that they appear older than they are or less attractive to others.
 Psychological problems due to baldness, if present, are typically most severe at the onset of symptoms.</p>
 <p>Hair loss induced by cancer chemotherapy has been reported to cause changes in self-concept and body image. 
 Body image does not return to the previous state after regrowth of hair for a majority of patients. In such cases, patients 
 have difficulties expressing their feelings (alexithymia) and may be more prone to avoiding family conflicts. 
 Family therapy can help families to cope with these psychological problems if they arise.</p>
