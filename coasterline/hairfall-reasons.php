<h5 class="color-blue">Lack of Nutrition</h5>
<p>Studies have shown that poor nutrition, limited food intake, and deficiencies in certain nutrients can cause thinning. 
These include deficiencies of biotin, protein, zinc and poor human iron metabolism, although complete baldness is not usually seen. 
A diet high in animal fats (often found in fast food) and vitamin A is also thought to lead to hair loss.</p>

