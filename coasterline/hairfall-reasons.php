<h4 class="color-blue">Causes</h4>
<h5 class="color-blue">Male pattern baldness</h5>
<p>More than 95% of hair loss in men is male pattern baldness (MPB). MPB is characterized by hair receding from the lateral sides of the
 forehead (known as a "receding hairline") and/or a thinning crown (balding to the area known as the 'vertex').
 Both become more pronounced until they eventually meet, leaving a horseshoe-shaped ring of hair around the back of the head.</p>
 <p>The incidence of pattern baldness varies from population to population and is based on genetic background. 
 Environmental factors do not seem to affect this type of baldness greatly. One large scale study in Maryborough, 
 Victoria, Australia showed the prevalence of mid-frontal baldness increases with age and affects 73.5 percent of men and 57 
 percent of women aged 80 and over. A rough rule of thumb is that the incidence of baldness in males corresponds roughly to chronological age. 
 For example, according to Medem Medical Library's website, MPB affects roughly 40 million men in the United States. 
 Approximately one in four men will have noticeable hair loss by age 30,
  and this figure increases to two in three men by age 60. In rare cases, MPB can begin as early as age 12.</p>
