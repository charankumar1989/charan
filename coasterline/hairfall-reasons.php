<h4 class="color-blue">Signs and Symptoms</h4>
<p>Symptoms of hairfall include hair loss in patches usually in circular patterns, dandruff, skin lesions, and scarring. 
Hairfall (mild - medium level) usually shows in unusual hair loss areas e.g. eyebrows, backside of the head or above 
the ears where usually the male pattern baldness does not affect. In male-pattern hair loss, loss and thinning begin at the temples and the 
crown and either thins out or falls out. Female-pattern hair loss occurs at the frontal and parietal.</p>

<h5 class="color-blue">Excessive daily hair loss</h5>
<p>People have between 100,000 and 150,000 hairs on their head. The number of strands normally lost in a day varies, 
but on average is 100. In order to maintain a normal volume, hair must be replaced at the same rate at which it is lost. The first signs of hair thinning that people will often notice are more hairs than usual left in the hairbrush after brushing or in the basin after shampooing. 
Styling can also reveal areas of thinning, such as a wider parting or a thinning crown.</p>

