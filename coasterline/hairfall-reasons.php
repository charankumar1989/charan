cystic acne arises from the same hormonal imbalances that cause hair loss, and is associated with dihydrotestosterone[clarification needed] 
production. Seborrheic dermatitis, a condition in which an excessive amount of sebum is produced and builds up on the scalp 
(looking like an adult cradle cap) is also a symptom of 
hormonal imbalances, as is an excessively oily or dry scalp. Both can cause hair thinning.</p>
